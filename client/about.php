<div class="box_message">
	<h2>About the Website</h2>
	<div class="box_text">
		Manual math provides a tool for the everyday person who is looking to learn, review, or see detailed examples of various mathematical subjects typically found at an undergraduate level. In order to appeal to the novice, the ordering of the menus follow, from top to bottom, what can be typically found in any given textbook. Each subject contains different topics, each topic contains different sections, and each section has notes and examples associated to it. The notes house a way to provide exactly what the section is about, sometimes with formal proofs and even explanations for the purpose of the mathematics at hand, while the examples provide concrete cases for the user to see the theory at work. The progression in examples typically increases with difficulty, thus providing a well balanced collection of different cases typically associated to the section. The goal of manual math is to administer new knowledge or clear up a misunderstanding that was present beforehand.
	</div>
</div>

