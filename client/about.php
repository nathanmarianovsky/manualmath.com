<div class="box_message">
	<h2>About the Name</h2>
	<div class="box_text">
		The inspiration behind manual math draws from the command line prompt known as "man" on linux. It is a shorthand for "manual" and is typically used to open the manual to some package. In this case, the package happens to be "math" which provides a mathematical library full of notes and examples on different sections.	
	</div>
</div>

